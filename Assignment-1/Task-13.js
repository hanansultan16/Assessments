
// To get the average of a series of values, you add the values up and then divide the sum by the number of values. 
// Write a complete program that stores the following values in five different variables: 28, 32, 37, 24 and 33. 
// The program should first calculate the sum of these five number and then stores then in a different variable named sum. 
// Then the program should display the average on screen. [Use data types correctly]


let value1 = 28;
let value2 = 32;
let value3 = 37;
let value4 = 24;
let value5 = 33;

let sumOfValues = value1 + value2 + value3 + value4 + value5;

let sum = sumOfValues / 5;

console.log(`The Average of the series is ${sum} `);
